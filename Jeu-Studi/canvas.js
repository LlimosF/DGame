// DICE 1

function diceOne () {
    const canvas = document.getElementById("canvas");
    const ctx = canvas.getContext("2d");
    ctx.beginPath();
    ctx.fillStyle="#FF4422"
    ctx.arc(60, 60, 8, 0, 2 * Math.PI);
    ctx.fill();
}

